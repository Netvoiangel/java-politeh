public class Main {
    public static void main(String[] args) {
        int intValue = 42;
        double doubleValue = 3.14;
        char charValue = 'A';
        boolean booleanValue = true;
        String stringValue = "Hello";

        String result1 = "Integer value: " + intValue; 
        String result2 = "Double value: " + doubleValue;
        String result3 = "Character value: " + charValue; 
        String result4 = "Boolean value: " + booleanValue; 
        String result5 = "Concatenation: " + stringValue + " " + intValue; 

        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);
        System.out.println(result5);
    }
}
