public class Main {
    public static void main(String[] args) {
        byte byteValue = 10;
        short shortValue = 20;
        int intValue = 30;
        long longValue = 40L;
        float floatValue = 50.0f;
        double doubleValue = 60.0;

        double result = byteValue + shortValue + intValue + longValue + floatValue + doubleValue;

        System.out.println("Result: " + result);
    }
}
